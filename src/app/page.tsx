async function handleCompress(imagePath: string) {
  try {
    const compressedPath = await invoke('compress_image', { path: imagePath, quality: 80 });
    console.log('Compressed image saved at:', compressedPath);
    alert(`文件已压缩并保存到: ${compressedPath}`); // 添加提示
  } catch (error) {
    console.error('Error compressing image:', error);
  }
}
